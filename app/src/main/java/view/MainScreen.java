    private void jLTasksAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLTasksAddMouseClicked
        // TODO add your handling code here:
        
        int indexSeletecProject = jLProjectsList.getSelectedIndex();
        if (indexSeletecProject != -1) {
            TaskDialogScreen taskDialogScreen = new TaskDialogScreen(this, true);

            Project project = (Project) projectsModel.get(jLProjectsList.getSelectedIndex());

            taskDialogScreen.setProject(project);
            taskDialogScreen.setVisible(true);

            taskDialogScreen.addWindowListener(new WindowAdapter() {
                public void windowClosed(WindowEvent e) {
                    int projectIndex = jLProjectsList.getSelectedIndex();
                    Project project = (Project) projectsModel.get(projectIndex);
                    try {
                        loadTasks(project.getId());
                    } catch (SQLException ex) {
                        Logger.getLogger(MainScreen.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            });
        } else {
            JOptionPane.showMessageDialog(rootPane, "Voc� deve escolher um projeto para essa tarefa", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLTasksAddMouseClicked

    private void jTableTaskListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableTaskListMouseClicked
        try {
            // TODO add your handling code here:
            int rowIndex = jTableTaskList.rowAtPoint(evt.getPoint());
            int columnIndex = jTableTaskList.columnAtPoint(evt.getPoint());
            Task task = taskTableModel.getTasks().get(rowIndex);
            
            switch (columnIndex) {
                case 1:
                    break;
                case 3:
                    taskController.update(task);
                    break;
                case 4:
                    JOptionPane.showMessageDialog(rootPane, "Editar a tarefa");
                    break;
                case 5:
                    taskController.deleteById(task.getId());
                    taskTableModel.getTasks().remove(task);

                    int projectIndex = jLProjectsList.getSelectedIndex();
                    Project project = (Project) projectsModel.get(projectIndex);
                    loadTasks(project.getId());
                    break;
            }
        } catch (SQLException ex) {
            Logger.getLogger(MainScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTableTaskListMouseClicked

