    public void initComponentsModel() throws SQLException{
        projectsModel = new DefaultListModel();
        loadProjects();
        
        taskTableModel = new TaskTableModel();
        jTableTaskList.setModel(taskTableModel);
        
        if(!projectsModel.isEmpty()){
            jLProjectsList.setSelectedIndex(0);
            int projectIndex = jLProjectsList.getSelectedIndex();
            Project project = (Project)projectsModel.get(projectIndex);
            loadTasks(project.getId());
        }
        
    }
    
    public void loadTasks(int project_id) throws SQLException{
        List<Task> tasks = taskController.getAll(project_id);
        
        taskTableModel.setTasks(tasks);
        showJTableTasks(!tasks.isEmpty());
        
    }
    
    private void showJTableTasks(boolean hasTasks){
        if(hasTasks) {
            if(jPEmptyList.isVisible()){
                jPEmptyList.setVisible(false);
                jPTaskList.remove(jPEmptyList);
            }
            
            jPTaskList.add(jSPTaskList);
            jSPTaskList.setVisible(true);
            jSPTaskList.setSize(jPTaskList.getWidth(), jPTaskList.getHeight());
        } else {
            if(jSPTaskList.isVisible()){
                jSPTaskList.setVisible(false);
                jPTaskList.remove(jSPTaskList);
            }
            
            jPTaskList.add(jPEmptyList);
            jPEmptyList.setVisible(true);
            jPEmptyList.setSize(jPTaskList.getWidth(), jPTaskList.getHeight());
        }
    }
    
    public void loadProjects() throws SQLException{
        List<Project> projects = projectController.getAll();
        projectsModel.clear();
        
        for (int i = 0; i < projects.size(); i++) {
            Project project = projects.get(i);
            projectsModel.addElement(project);
        }
        
        jLProjectsList.setModel(projectsModel);
        
    }
